# Assigning a Function to a Variable

# We define a function called 'hello' that prints "Hello!"
def hello():
    print("Hello!")

# Assign the function object 'hello' to a new variable 'hi'
hi = hello

# Call the function using the new variable 'hi'
# This will execute the 'hello' function and print "Hello!"
hi()


# Assigning the 'print' function to a variable 'say'
say = print

# Call the function using the variable 'say'
# This will execute the 'print' function and print "Woah! This is cool!"
say("Woah! This is cool!")
